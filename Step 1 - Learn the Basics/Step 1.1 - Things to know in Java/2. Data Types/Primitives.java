class Primitives {
    public static void main(String[] args) {
        boolean isValid = true;
        System.out.println("Boolean value isValid = " + isValid);

        byte marks = 99;
        System.out.println("Byte value marks = " + marks);

        float pi = 3.14f;
        System.out.println("Float value pi = " + pi);

        float div = 15.0f / 4.0f;
        System.out.println("Float value div = " + div);

        long views = 1000000000;
        System.out.println("Long value views = " + views);

        char gender = 'M';
        System.out.println("Char value gender = " + gender);

    }
}